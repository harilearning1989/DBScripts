        SELECT 'Mumbai', 'Dadar' FROM DUAL UNION ALL
        SELECT 'Mumbai', 'Thane' FROM DUAL UNION ALL
        SELECT 'Anantapur', 'Anantapur Junction' FROM DUAL UNION ALL
        SELECT 'Anantapur', 'Dharmavaram Junction' FROM DUAL UNION ALL
        SELECT 'Anantapur', 'Gooty' FROM DUAL UNION ALL
        SELECT 'Thirupathi', 'Thirupathi Main' FROM DUAL UNION ALL
        SELECT 'Thirupathi', 'Renigunta Junction' FROM DUAL UNION ALL
        SELECT 'Thirupathi', 'Chandragiri' FROM DUAL UNION ALL
        SELECT 'Chennai', 'Egmore' FROM DUAL UNION ALL
        SELECT 'Chennai', 'Chennai Central' FROM DUAL UNION ALL
        SELECT 'Chennai', 'Tambaram' FROM DUAL UNION ALL
        SELECT 'Chittoor', 'Chittoor Junction' FROM DUAL UNION ALL
        SELECT 'Chittoor', 'Madanapalle Road' FROM DUAL UNION ALL
        SELECT 'Chittoor', 'Pakala Junction' FROM DUAL
    ) LOOP
        INSERT INTO STATION_DETAILS (
            station_name, location, platform_count, is_junction, updated_date
        ) VALUES (
            city_station.station,                            -- Station name
            city_station.city,                               -- City
            TRUNC(DBMS_RANDOM.VALUE(1, 11)),                 -- Random platform count between 1 and 10
            CASE MOD(DBMS_RANDOM.VALUE(1, 100), 2) WHEN 0 THEN 'Y' ELSE 'N' END, -- Random 'Y' or 'N'
            SYSDATE - TRUNC(DBMS_RANDOM.VALUE(1, 1000))      -- Random updated date within ~3 years
        );
END LOOP;
END;

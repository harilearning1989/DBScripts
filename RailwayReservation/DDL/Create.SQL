CREATE TABLE STATION_DETAILS
(
    STATION_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STATION_NAME   VARCHAR2(100) NOT NULL,
    LOCATION       VARCHAR2(100) NOT NULL,
    PLATFORM_COUNT NUMBER NOT NULL CHECK (platform_count BETWEEN 1 AND 10), -- Ensure single digit
    IS_JUNCTION    CHAR(1) CHECK (is_junction IN ('Y', 'N')),

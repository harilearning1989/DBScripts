    UPDATED_AT     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT unique_station UNIQUE (station_name, location)               -- Ensure uniqueness
);

BEGIN
FOR city_station IN (
        SELECT 'Hyderabad' AS city, 'Kachiguda' AS station FROM DUAL UNION ALL

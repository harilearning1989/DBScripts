CREATE TABLE TRAIN_DETAILS
(
    id             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,                    -- Auto-incremented primary key
    train_number   NUMBER    NOT NULL UNIQUE,                                              -- Unique train number
    train_name     VARCHAR2(100) NOT NULL UNIQUE,                                          -- Unique train name
    source         VARCHAR2(50) NOT NULL,                                                  -- Starting location
    destination    VARCHAR2(50) NOT NULL,                                                  -- Ending location
    departure_time TIMESTAMP NOT NULL,                                                     -- Departure time
    arrival_time   TIMESTAMP NOT NULL,                                                     -- Arrival time
    duration       VARCHAR2(10) NOT NULL,                                                  -- Travel duration (e.g., 12:00 for 12 hours)
    train_type     VARCHAR2(20) NOT NULL,                                                  -- Type of train
    total_seats    NUMBER    NOT NULL,                                                     -- Total number of seats available
    fare           NUMBER(10, 2) NOT NULL,                                                 -- Ticket price with up to 2 decimal places
    halts          CLOB      NOT NULL,                                                     -- List of stations where the train halts
    CONSTRAINT chk_train_type CHECK (train_type IN ('Passenger', 'Express', 'Super Fast')) -- Valid values for train type
);

CREATE TABLE SCHEDULE_DETAILS
(
    id                   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRAIN_ID             NUMBER,
    DEPARTURE_STATION_ID NUMBER,
    ARRIVAL_STATION_ID   NUMBER,
    TRAVEL_DATE          DATE      NOT NULL,
    DEPARTURE_TIME       TIMESTAMP NOT NULL,
    ARRIVAL_TIME         TIMESTAMP NOT NULL,
    AVAILABLE_SEATS      NUMBER,
    FARE                 NUMBER(10, 2) NOT NULL,
    STATUS               VARCHAR2(10) CHECK (STATUS IN ('SCHEDULED', 'TRAVELED', 'CANCELED', 'DELAYED')),
    CONSTRAINT FK_SCHEDULE_DETAILS_TRAIN_ID FOREIGN KEY (TRAIN_ID) REFERENCES TRAIN_DETAILS (ID),
    CONSTRAINT FK_SCHEDULE_DETAILS_DEPARTURE_STATION_ID FOREIGN KEY (DEPARTURE_STATION_ID) REFERENCES STATION_DETAILS (STATION_ID),
    CONSTRAINT FK_SCHEDULE_DETAILS_ARRIVAL_STATION_ID FOREIGN KEY (ARRIVAL_STATION_ID) REFERENCES STATION_DETAILS (STATION_ID)
);

CREATE TABLE SCHEDULE_TRAIN
(
    id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRAIN_ID     NUMBER,
    STATUS       VARCHAR2(10) CHECK (STATUS IN ('SCHEDULED', 'TRAVELED', 'CANCELED', 'DELAYED')),
    SCHEDULED_BY VARCHAR2(50) NOT NULL,
    CREATED_AT   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_SCHEDULE_TRAIN_ID FOREIGN KEY (TRAIN_ID) REFERENCES TRAIN_DETAILS (ID)
);

ALTER TABLE SCHEDULE_TRAIN

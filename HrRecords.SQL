CREATE TABLE HR_RECORDS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_ID NUMBER,
    NAME_PREFIX VARCHAR2(5),
    FIRST_NAME VARCHAR2(10),
    MIDDLE_INITIAL VARCHAR2(1),
    LAST_NAME VARCHAR2(12),
    GENDER VARCHAR2(1),
    E_MAIL VARCHAR2(34),
    FATHER_S_NAME VARCHAR2(20),
    MOTHER_S_NAME VARCHAR2(20),
    MOTHER_S_MAIDEN_NAME VARCHAR2(13),
    DATE_OF_BIRTH VARCHAR2(10),
    TIME_OF_BIRTH VARCHAR2(11),
    AGE_IN_YRS VARCHAR2(2),
    WEIGHT_IN_KGS VARCHAR2(5),
    DATE_OF_JOINING VARCHAR2(10),
    QUARTER_OF_JOINING VARCHAR2(2),
    HALF_OF_JOINING VARCHAR2(1),
    YEAR_OF_JOINING NUMBER,
    MONTH_OF_JOINING VARCHAR2(2),
    MONTH_NAME_OF_JOINING VARCHAR2(9),
    SHORT_MONTH VARCHAR2(3),
    DAY_OF_JOINING VARCHAR2(2),
    DOW_OF_JOINING VARCHAR2(9),
    SHORT_DOW VARCHAR2(3),
    AGE_IN_COMPANY_YEARS VARCHAR2(2),
    SALARY NUMBER,
    LAST_HIKE VARCHAR2(6),
    SSN VARCHAR2(11),
    PHONE_NO VARCHAR2(14),
    PLACE_NAME VARCHAR2(24),
    COUNTY VARCHAR2(20),
    CITY VARCHAR2(20),
    STATE VARCHAR2(2),
    ZIP VARCHAR2(5),
    REGION VARCHAR2(10),
    USER_NAME VARCHAR2(25),
    PASSWORD VARCHAR2(20)
);

CREATE TABLE HR_RECORDS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_ID NUMBER,
    NAME_PREFIX VARCHAR2(5),
    FIRST_NAME VARCHAR2(15),
    MIDDLE_INITIAL VARCHAR2(1),
    LAST_NAME VARCHAR2(15),
    GENDER VARCHAR2(1),
    E_MAIL VARCHAR2(40),
    FATHER_S_NAME VARCHAR2(25),
    MOTHER_S_NAME VARCHAR2(25),
    MOTHER_S_MAIDEN_NAME VARCHAR2(13),
    DATE_OF_BIRTH VARCHAR2(10),
    TIME_OF_BIRTH VARCHAR2(11),
    AGE_IN_YRS VARCHAR2(5),
    WEIGHT_IN_KGS VARCHAR2(5),
    DATE_OF_JOINING VARCHAR2(10),
    QUARTER_OF_JOINING VARCHAR2(2),
    HALF_OF_JOINING VARCHAR2(3),
    YEAR_OF_JOINING NUMBER,
    MONTH_OF_JOINING VARCHAR2(2),
    MONTH_NAME_OF_JOINING VARCHAR2(9),
    SHORT_MONTH VARCHAR2(3),
    DAY_OF_JOINING VARCHAR2(2),
    DOW_OF_JOINING VARCHAR2(9),
    SHORT_DOW VARCHAR2(3),
    AGE_IN_COMPANY_YEARS VARCHAR2(5),
    SALARY NUMBER,
    LAST_HIKE VARCHAR2(6),
    SSN VARCHAR2(11),
    PHONE_NO VARCHAR2(14),
    PLACE_NAME VARCHAR2(30),
    COUNTY VARCHAR2(25),
    CITY VARCHAR2(25),
    STATE VARCHAR2(2),
    ZIP VARCHAR2(5),
    REGION VARCHAR2(10),
    USER_NAME VARCHAR2(25),
    PASSWORD VARCHAR2(20)
);


CREATE TABLE "EMPLOYEE"."HR_RECORDS"
(	
ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
"EMP_ID" NUMBER,
     "NAME_PREFIX" VARCHAR2(5 BYTE),
     "FIRST_NAME" VARCHAR2(15 BYTE),
     "MIDDLE_INITIAL" VARCHAR2(1 BYTE),
     "LAST_NAME" VARCHAR2(15 BYTE),
     "GENDER" VARCHAR2(1 BYTE),
     "E_MAIL" VARCHAR2(40 BYTE),
     "FATHER_S_NAME" VARCHAR2(25 BYTE),
     "MOTHER_S_NAME" VARCHAR2(25 BYTE),
     "MOTHER_S_MAIDEN_NAME" VARCHAR2(13 BYTE),
     "DATE_OF_BIRTH" VARCHAR2(10 BYTE),
     "TIME_OF_BIRTH" VARCHAR2(11 BYTE),
     "AGE_IN_YRS" VARCHAR2(5 BYTE),
     "WEIGHT_IN_KGS" VARCHAR2(5 BYTE),
     "DATE_OF_JOINING" VARCHAR2(10 BYTE),
     "QUARTER_OF_JOINING" VARCHAR2(2 BYTE),
     "HALF_OF_JOINING" VARCHAR2(3 BYTE),
     "YEAR_OF_JOINING" NUMBER,
     "MONTH_OF_JOINING" VARCHAR2(2 BYTE),
     "MONTH_NAME_OF_JOINING" VARCHAR2(9 BYTE),
     "SHORT_MONTH" VARCHAR2(3 BYTE),
     "DAY_OF_JOINING" VARCHAR2(2 BYTE),
     "DOW_OF_JOINING" VARCHAR2(9 BYTE),
     "SHORT_DOW" VARCHAR2(3 BYTE),
     "AGE_IN_COMPANY_YEARS" VARCHAR2(5 BYTE),
     "SALARY" NUMBER,
     "LAST_HIKE" VARCHAR2(6 BYTE),
     "SSN" VARCHAR2(11 BYTE),
     "PHONE_NO" VARCHAR2(14 BYTE),
     "PLACE_NAME" VARCHAR2(30 BYTE),
     "COUNTY" VARCHAR2(25 BYTE),
     "CITY" VARCHAR2(25 BYTE),
     "STATE" VARCHAR2(2 BYTE),
     "ZIP" VARCHAR2(5 BYTE),
     "REGION" VARCHAR2(10 BYTE),
     "USER_NAME" VARCHAR2(25 BYTE),
     "PASSWORD" VARCHAR2(20 BYTE),
     "DEPARTMENT" VARCHAR2(20 BYTE) DEFAULT 'Admin' NOT NULL ENABLE,
     "DEPARTMENT_ID" NUMBER,
     PRIMARY KEY ("ID"),
     CONSTRAINT "FK_DEPARTMENT_ID" FOREIGN KEY ("DEPARTMENT_ID")
         REFERENCES "EMPLOYEE"."DEPARTMENTS" ("DEPARTMENT_ID") ENABLE
);
